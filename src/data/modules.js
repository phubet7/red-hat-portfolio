// Helper to get a module by id
export function getModule(id) {
  return modules.find((m) => m.id === id)
}

// Flatten all graded activities across modules for progress tracking
export function getAllModuleActivities(moduleId) {
  const m = getModule(moduleId)
  if (!m) return []
  const activities = [
    // each lesson counts as a completion activity (non-graded)
    ...m.lessons.map((l) => ({ type: 'lesson', id: l.id })),
    { type: 'quiz', id: `${moduleId}-quiz` },
    { type: 'scenario', id: `${moduleId}-scenario` },
  ]
  return activities
}
